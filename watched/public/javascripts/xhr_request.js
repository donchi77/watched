function addFilm(idFilm, title, status, poster) {
    let xhr = new XMLHttpRequest();
    let params = `id=${idFilm}&title=${title}&poster=${poster}&status=${status}`;
    console.log(params);
    
    xhr.onload = () => {
        let data = JSON.parse(xhr.responseText);
        console.log(data);

        if (data.response) {
            changeIcon(data);
        }
    };

    xhr.onerror = (err) => {
        console.error(err);
    };

    xhr.open('POST', '/data/watched');
    xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
    xhr.send(params);
}

function checkStatus(idFilm) {
    console.log(idFilm);
    let xhr = new XMLHttpRequest();
    let params = 'id=' + idFilm;

    xhr.onload = () => {
        let data = JSON.parse(xhr.responseText);
        console.log(data)

        if (data.response)
            changeIcon(data);
    };

    xhr.onerror = (err) => {
        console.error(err);
    };

    xhr.open('GET', '/data/status?' + params);
    xhr.send();
}

